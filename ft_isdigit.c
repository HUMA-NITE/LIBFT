#include "libft.h"

/*
** Return 1 if c is a digit, 0 if not.
** Retourne 1 si c est un chiffre, 0 sinon.
*/

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
