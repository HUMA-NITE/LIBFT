#include "libft.h"

/*
** Return 1 if c is alpha, 0 if not.
** Retourne 1 si c est alphabetique, 0 sinon.
*/

int	ft_isalpha(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}
